package com.playground.qatests;

public class RecurringInfo {
	private String amount;
	private String create;
	private String limit;
	private String periodic_number;
	private String periodic_type;
	private String startDate;
	
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getCreate() {
		return create;
	}
	public void setCreate(String create) {
		this.create = create;
	}
	public String getLimit() {
		return limit;
	}
	public void setLimit(String limit) {
		this.limit = limit;
	}
	public String getPeriodic_number() {
		return periodic_number;
	}
	public void setPeriodic_number(String periodic_number) {
		this.periodic_number = periodic_number;
	}
	public String getPeriodic_type() {
		return periodic_type;
	}
	public void setPeriodic_type(String periodic_type) {
		this.periodic_type = periodic_type;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	
}
